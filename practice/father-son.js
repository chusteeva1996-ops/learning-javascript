// Функция находит, через сколько лет (или лет назад) отец будет вдвое старше сына
function yearsToDouble(father, son) {
    
    // Начинаем с 0 лет и постепенно увеличиваем
    // Мы не знаем, в прошлом это или будущем, поэтому проверяем оба направления
    for (let years = 0; ; years++) {
        
        // ПРОВЕРКА ПРОШЛОГО (years лет назад)
        // Проверяем, не станет ли возраст отрицательным
        // Если отцу или сыну years лет назад было бы меньше 0 - пропускаем
        if (father - years >= 0 && son - years >= 0) {
            // years лет назад: отцу было (father - years), сыну (son - years)
            // Проверяем: отец был вдвое старше сына?
            if ((father - years) === 2 * (son - years)) {
                return years;  // Нашли решение в прошлом
            }
        }
        
        // ПРОВЕРКА БУДУЩЕГО (years лет вперёд)
        // Через years лет: отцу будет (father + years), сыну (son + years)
        if ((father + years) === 2 * (son + years)) {
            return years;  // Нашли решение в будущем
        }
        
        // Если не нашли - продолжаем цикл с years + 1
        // Цикл бесконечный, но решение найдётся всегда
    }
}
